/**
 * Pro-athlete roster ingest — Stage 1 (WTCS / World Triathlon).
 *
 * Reads the ELITE start lists of upcoming World Triathlon Championship Series
 * events from the official API and turns each starter into a pro athlete for the
 * app (id, name, ISO-2 country, gender, series, birth year, WT profile link) plus
 * their upcoming starts — each start linking to the official event page where the
 * start list lives. Elite programs only → never age groupers.
 *
 * Pure Node built-ins (no deps) so CI needs no install. Writes
 * src/data/proAthletes.json; the app merges it on top of the curated roster
 * (curated ids win), so every starter gets a full profile automatically.
 *
 * Other circuits (IRONMAN / 70.3 / T100 / PTO) publish JS-rendered start lists →
 * a separate headless-browser robot will add those. See docs/pro-athletes.md.
 *
 * Usage: node scripts/ingest-pro-athletes.mjs [--days=150]
 */
import { writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const OUT = resolve(ROOT, 'src/data/proAthletes.json');

const BASE = 'https://api.triathlon.org/v1';
const API_KEY = process.env.EXPO_PUBLIC_WT_API_KEY || '2649776ef9ece4c391003b521cbfce7a';
const WTCS_CATEGORY = 351;
const DAYS = Number((process.argv.find((a) => a.startsWith('--days=')) || '').split('=')[1]) || 150;

async function wt(path, params = {}) {
  const qs = new URLSearchParams(Object.entries(params).map(([k, v]) => [k, String(v)])).toString();
  const ctrl = new AbortController();
  const t = setTimeout(() => ctrl.abort(), 20000);
  try {
    const r = await fetch(`${BASE}${path}${qs ? `?${qs}` : ''}`, {
      headers: { apikey: API_KEY, Accept: 'application/json' },
      signal: ctrl.signal,
    });
    if (!r.ok) return null;
    return (await r.json())?.data ?? null;
  } catch {
    return null;
  } finally {
    clearTimeout(t);
  }
}

const ymd = (d) => new Date(d).toISOString().slice(0, 10);
const slug = (s) =>
  s.toLowerCase().normalize('NFD').replace(/[̀-ͯ]/g, '').replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');

/** "2026 World Triathlon Championship Series Hamburg" → "WTCS Hamburg". */
const shortName = (title, venue) =>
  venue ? `WTCS ${venue}` : title.replace(/^\d{4}\s+/, '').replace(/World Triathlon Championship Series/i, 'WTCS').trim();

async function main() {
  const events =
    (await wt('/events', {
      category_id: WTCS_CATEGORY,
      start_date: ymd(Date.now()),
      end_date: ymd(Date.now() + DAYS * 864e5),
      per_page: 50,
    })) || [];
  console.log(`WTCS events in the next ${DAYS} days: ${events.length}`);

  const athletes = new Map(); // id -> athlete
  const starts = new Map(); // id -> [start]

  for (const ev of events) {
    const programs = (await wt(`/events/${ev.event_id}/programs`)) || [];
    const elite = programs.filter((p) => /^elite (men|women)$/i.test(p.prog_name || ''));
    let n = 0;
    for (const prog of elite) {
      const gender = /women/i.test(prog.prog_name) ? 'women' : 'men';
      const data = await wt(`/events/${ev.event_id}/programs/${prog.prog_id}/entries`);
      const entries = data?.entries || (Array.isArray(data) ? data : []);
      for (const e of entries) {
        const name = (e.athlete_full_name || e.athlete_title || '').trim();
        const iso2 = e.athlete_country_isoa2;
        if (!name || !iso2) continue;
        const id = slug(name);
        if (!athletes.has(id)) {
          const yob = Number(e.athlete_yob);
          athletes.set(id, {
            id,
            name,
            country: iso2,
            gender,
            series: ['wtcs'],
            ...(yob >= 1970 && yob <= 2012 ? { birthYear: yob } : {}),
            ...(e.athlete_id && e.athlete_slug
              ? { links: { website: `https://www.triathlon.org/athletes/profile/${e.athlete_id}/${e.athlete_slug}` } }
              : {}),
          });
        }
        const list = starts.get(id) || [];
        if (!list.some((s) => s.event === shortName(ev.event_title, ev.event_venue))) {
          list.push({
            date: ev.event_date,
            event: shortName(ev.event_title, ev.event_venue),
            series: 'wtcs',
            location: [ev.event_venue, ev.event_country].filter(Boolean).join(', '),
            url: ev.event_listing, // official event page incl. the start list
          });
          starts.set(id, list);
        }
        n++;
      }
    }
    console.log(`· ${shortName(ev.event_title, ev.event_venue)} (${ev.event_date}): ${n} elite starters`);
  }

  const out = {
    generatedAt: new Date().toISOString(),
    note: 'Pro athletes + upcoming starts ingested from World Triathlon ELITE start lists (WTCS). Never age groupers. Merged onto the curated roster (curated ids win). Regenerated by the data pipeline; do not hand-edit. Other circuits (IRONMAN/70.3/T100/PTO) need a headless-browser robot.',
    athletes: [...athletes.values()].sort((a, b) => a.name.localeCompare(b.name)),
    starts: Object.fromEntries(starts),
  };
  await writeFile(OUT, JSON.stringify(out, null, 2) + '\n');
  console.log(`\nWrote ${out.athletes.length} pro athletes (+ upcoming starts) → ${OUT}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
